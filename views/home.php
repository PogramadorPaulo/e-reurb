            <div class="p-2 text-center">
              <h4 class="mt-4">O que é o Reurb?</h4><br>
              <p class="text-muted">De acordo com a <a href="http://www.planalto.gov.br/ccivil_03/_ato2015-2018/2017/lei/l13465.htm" target="_blank">Lei n° 13.465, de 2017</a>, a REURB é o conjunto de medidas jurídicas, urbanísticas, ambientais
                e sociais destinadas à incorporação dos núcleos urbanos
                informais ao ordenamento territorial urbano e à titulação de seus ocupantes.</p>
              <p class="text-muted"><b>REURB-S</b> o qual é destinado a pessoas de baixa renda, especificamente autorizados a esse enquadramento mediante requisitos próprios.
                É o chamado REURB DE INTERESSE SOCIAL;<br>
                O <b>REURB-E</b> destinado a pessoas enquadradas em outros grupos, mediante análise de cada grupo e das condições que adquiriram os imóveis.</p><br>
              <h4 class="mt-2">Quem tem direito a Reurb?</h4><br>
              <p class="text-muted">A Reurb poderá ser promovida por loteadores e incorporadores que deram origem aos núcleos urbanos informais, sem prejuízo das sanções cabíveis (penais, cíveis, administrativas),
                decorrentes de irregularidades no loteamento ou na incorporação até o ano de 2016.</p>
              <p class="text-muted">
                "Conforme art. 17 da <a href="http://www.planalto.gov.br/ccivil_03/_ato2019-2022/2022/lei/L14382.htm" target="_blank">Lei 14.382/22</a>, que alterou o Art. 76, §1º da Lei 13.465/17, o procedimento administrativo e os atos de registro decorrentes da
                <b>Reurb deverão ser feitos por meio eletrônico</b>".
              </p>
